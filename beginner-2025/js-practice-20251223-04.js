// ==================================================
// 📘 정규식 연습 문제 — ⭐⭐⭐ 난이도 (1 ~ 10)
// 포인트: 형식 검사 / 조건 조합
// ==================================================


// --------------------------------------------------
// 문제 1 (⭐⭐⭐) — 아이디 형식 검사 (기초)
// --------------------------------------------------
// 조건:
// - 영문자 + 숫자만 사용
// - 5자 이상
//
// 출력값: true
// --------------------------------------------------

const q1 = "user123";
// 👉 코드 작성
console.log(/^[a-zA-Z0-9]{5,}$/.test(q1));



// --------------------------------------------------
// 문제 2 (⭐⭐⭐) — 숫자 + 영문자 조합인지
// --------------------------------------------------
// 조건:
// - 영문자 또는 숫자만 사용
// - 숫자가 반드시 1개 이상 포함
//
// 출력값: true
// --------------------------------------------------

const q2 = "test9";
// 👉 코드 작성
console.log(/^(?=.*\d)[a-zA-Z0-9]+$/.test(q2));
// (?=.*\d) : 숫자가 최소 1개는 반드시 있어야 한다


// --------------------------------------------------
// 문제 3 (⭐⭐⭐) — 비밀번호 길이 검사
// --------------------------------------------------
// 조건:
// - 어떤 문자든 상관없음
// - 길이 8자 이상
//
// 출력값: true
// --------------------------------------------------

const q3 = "pass!123";
// 👉 코드 작성
console.log(/^.{8,}$/.test(q3));
// . (dot)은? 줄바꿈을 제외한 “아무 문자 1개”


// --------------------------------------------------
// 문제 4 (⭐⭐⭐) — 영문자로 시작하는 아이디
// --------------------------------------------------
// 조건:
// - 영문자로 시작
// - 영문자 + 숫자만 사용
// - 4자 이상
//
// 출력값: true
// --------------------------------------------------

const q4 = "a123";
// 👉 코드 작성
console.log(/^[a-zA-Z][a-zA-Z0-9]{4,}$/.test(q4));



// --------------------------------------------------
// 문제 5 (⭐⭐⭐) — 숫자로 끝나는 코드인지
// --------------------------------------------------
// 조건:
// - 영문자 + 숫자만 사용
// - 숫자로 끝나야 함
//
// 출력값: true
// --------------------------------------------------

const q5 = "item7";
// 👉 코드 작성
console.log(/[a-zA-Z]+\d$/.test(q5));


// --------------------------------------------------
// 문제 6 (⭐⭐⭐) — 공백 없는 문자열 + 길이 제한
// --------------------------------------------------
// 조건:
// - 공백 없이 구성
// - 길이 6자 이상
//
// 출력값: true
// --------------------------------------------------

const q6 = "nospace";
// 👉 코드 작성
console.log(/^\S{6,}$/.test(q6));


// --------------------------------------------------
// 문제 7 (⭐⭐⭐) — 이메일 형식 (아주 기초)
// --------------------------------------------------
// 조건:
// - 영문자 + 숫자 조합
// - @ 포함
// - .com 으로 끝남
//
// 출력값: true
// --------------------------------------------------

const q7 = "user123@test.com";
// 👉 코드 작성
console.log(/^[a-zA-Z0-9]+@[a-zA-Z0-9]+\.com$/.test(q7));


// --------------------------------------------------
// 문제 8 (⭐⭐⭐) — 특수문자 포함 비밀번호
// --------------------------------------------------
// 조건:
// - 길이 8자 이상
// - ! 또는 @ 반드시 포함
//
// 출력값: true
// --------------------------------------------------

const q8 = "pass!word";
// 👉 코드 작성
console.log(/^(?=.*[!@]).{8,}$/.test(q8));



// --------------------------------------------------
// 문제 9 (⭐⭐⭐) — 숫자만으로 6~8자리인지
// --------------------------------------------------
// 출력값: true
// --------------------------------------------------

const q9 = "123456";
// 👉 코드 작성
console.log(/^\d{6,8}$/.test(q9));


// --------------------------------------------------
// 문제 10 (⭐⭐⭐) — 영문자 + 숫자 조합, 소문자 필수
// --------------------------------------------------
// 조건:
// - 영문자 + 숫자만 사용
// - 소문자 최소 1개 포함
// - 길이 5자 이상
//
// 출력값: true
// --------------------------------------------------

const q10 = "abC12";
// 👉 코드 작성
console.log(/^(?=.*[a-z])[a-zA-Z0-9]{5,}$/.test(q10));

// 전체 검사            /^...$/

// 필수 포함 조건
// (?=.*\d)      // 숫자 필수
// (?=.*[a-z])   // 소문자 필수
// (?=.*[!@])    // 특수문자 필수