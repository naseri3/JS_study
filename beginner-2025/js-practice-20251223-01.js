// 👉 코드 작성
console.log(/^010-\d{4}-\d{4}$/.test(phone));



// --------------------------------------------------
// 문제 5 (⭐⭐⭐) — 이메일 형식 검사 (기초)
// --------------------------------------------------
// 조건:
// - 영문자 + 숫자 조합
// - @ 포함
// - .com 으로 끝나야 함
//
// 출력값:
// true
// --------------------------------------------------

const email2 = "user123@test.com";

// 👉 코드 작성
