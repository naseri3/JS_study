// --------------------------------
// 문제 1 — 숫자 더하기
// 👉 숫자 두 개를 변수로 선언하고 두 수의 합을 콘솔에 출력하시오.
//
/*
  요구사항:
  - let 또는 const 사용
  - console.log() 사용
*/
const num1 = 10;
const num2 = 5;
console.log( num1 + num2 );


console.log("--------------------------------");


// --------------------------------
// 문제 2 — 문자열 길이 구하기
// 👉 아래 문자열의 길이를 콘솔에 출력하시오.
//
/*
  힌트:
  - 문자열길이: 변수명.length
*/
let text = "JavaScript";
console.log(text.length);


console.log("--------------------------------");


// --------------------------------
// 문제 3 — 조건문 기초
// 👉 age 값이 18 이상이면 "성인입니다"
//    아니라면 "미성년자입니다" 출력.
//
/*
  힌트:
  if (조건) {
    실행문
  } else {
    실행문
  }
*/
let age = 20;
if (age >= 18) {
  console.log("성인입니다");
} else {
  console.log("미성년자입니다");
}


console.log("--------------------------------");


// --------------------------------
// 문제 4 — 배열 값 출력
// 👉 아래 배열의 첫 번째 요소를 콘솔에 출력하시오.
//
/*
  힌트:
  배열은 index 0부터 시작
*/
let fruits = ["apple", "banana", "orange"];
console.log(fruits[0]);


console.log("--------------------------------");


// --------------------------------
/*
문제 5 — while 반복문
👉 1부터 5까지 숫자를 while문으로 출력하시오.

힌트:
- let i = 1;
- while (조건) {
    console.log(i);
    i++;
  }
*/
let i = 1;
while (i <= 5) {
    console.log(i);
    i++;
}


console.log("--------------------------------");


